package com.example.demo.java8examples;

public class Student {
	int x = 20;
	String name = "Testing";

	public Student() {

	}

	public Student(int x, String name) {
		this.x = x;
		this.name = name;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
